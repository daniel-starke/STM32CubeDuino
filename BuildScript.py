def mcuAsDefine(mcu):
	""" Coverts the MCU to a proper CPP define and returns it. """
	isProperCased = True
	for c in mcu:
		if c.islower() and c != "x":
			isProperCased = False
			break
	return mcu if isProperCased else mcu.upper()

def getMcuVariant(boardId, cpu, mcu):
	""" Get part number, variant, define, core architecture, ram and flash size (KiB) for the current board configuration and perform some validity checks. """
	result = {
		"partNumber": None,
		"variant": None,
		"define": None,
		"ram": None,
		"flash": None
	}
	lowerCpu = cpu.lower()
	lowerMcu = mcu.lower()
	selectedTarget = None
	for target in mcuList:
		partNumber = None
		for pn in target[0]:
			if lowerMcu.startswith(pn.lower()) or re.match(pn.replace("x", "."), lowerMcu, re.I):
				partNumber = pn
				break
		if partNumber == None:
			continue
		variant = None
		variants = ""
		for v in target[1]:
			fullName = partNumber.lower() + v.lower()
			if lowerMcu == fullName or re.match(fullName.replace("x", "."), lowerMcu, re.I):
				variant = v
				break
			if variants != "":
				variants += ", "
			variants += v
		if variant == None:
			return {"error": "Unknown variant in MCU field for board %s. Possible variants for %s are: %s." % (boardId, partNumber, variants)}
		selectedTarget = target
		result["partNumber"] = partNumber
		result["variant"] = variant
		result["define"] = target[2]
		result["ram"] = target[4]
		result["flash"] = target[5]
		break
	if selectedTarget == None:
		return {"error": "Unknown value in MCU field for board %s." % boardId}
	hasCore = False
	cores = ""
	for core in selectedTarget[3]:
		if lowerCpu == core.lower():
			hasCore = True
			break
		if cores != "":
			cores += ", "
		cores += core
	if hasCore == False:
		return {"error": "Invalid value in CPU field for board %s. Possible values for %s are: %s." % (boardId, result["partNumber"] + result["variant"], cores)}
	return result

def getSvdFile(mcu, warn = False):
	""" Returns the PlatformIO SVD file name for the given MCU. """
	from platformio.project.config import ProjectConfig
	projectConfig = ProjectConfig()
	corePath = projectConfig.get_optional_dir("core")
	if corePath:
		svdMapPath = os.path.join(corePath, "platforms", "ststm32", "misc", "svd", "Contents.txt")
		try:
			with open(svdMapPath, "r") as fp:
				for line in fp:
					mapping = map(str.strip, line.split(","))
					if mcu in mapping:
						return list(mapping)[-1]
		except IOError:
			if warn:
				print >>sys.stderr, "Warning: SVD file name list file not found: " + svdMapPath
	return None


